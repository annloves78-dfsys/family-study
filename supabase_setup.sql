-- 2. daily_targets (날짜별 목표 도장 개수)
CREATE TABLE daily_targets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE,
    date_str TEXT NOT NULL, -- YYYY-MM-DD
    target_count INTEGER DEFAULT 0,
    UNIQUE(user_id, date_str)
);

-- 3. study_stamps (찍은 도장 기록)
CREATE TABLE study_stamps (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE,
    date_str TEXT NOT NULL, -- YYYY-MM-DD
    stamp_index INTEGER NOT NULL, -- 0, 1, 2... (몇 번째 도장인지)
    is_coupon BOOLEAN DEFAULT false, -- 쿠폰으로 찍은 빈 칸인지 여부
    UNIQUE(user_id, date_str, stamp_index)
);

-- 4. custom_events (용돈 지급/회수 등 이벤트 기록)
CREATE TABLE custom_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE,
    event_type TEXT NOT NULL, -- 'money', 'coupon'
    amount INTEGER NOT NULL, -- positive or negative
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- ==========================================
-- 권한 설정 (RLS 비활성화 - 누구나 읽고 쓰기 가능)
-- 가족끼리만 쓰는 앱이므로 편의를 위해 보안을 풀고 자유롭게 씁니다.
-- ==========================================
ALTER TABLE profiles DISABLE ROW LEVEL SECURITY;
ALTER TABLE daily_targets DISABLE ROW LEVEL SECURITY;
ALTER TABLE study_stamps DISABLE ROW LEVEL SECURITY;
ALTER TABLE custom_events DISABLE ROW LEVEL SECURITY;
