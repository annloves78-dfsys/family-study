-- =========================================================
-- 이 코드를 복사해서 Supabase 대시보드의 'SQL Editor' 탭에 붙여넣고 RUN(실행) 버튼을 누르시면 됩니다.

-- 1. profiles 테이블 생성
CREATE TABLE profiles (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    grade TEXT NOT NULL,
    school_code TEXT NOT NULL,
    money INTEGER DEFAULT 0,
    coupons INTEGER DEFAULT 0,
    accumulated_extra_minutes INTEGER DEFAULT 0,
    last_study_date TEXT DEFAULT '',
    today_studied_minutes INTEGER DEFAULT 0,
    target_weekday INTEGER DEFAULT 120,
    target_weekend INTEGER DEFAULT 60,
    target_vacation INTEGER DEFAULT 120
);

