-- 기존 테이블이 있다면 깨끗하게 지우고 새로 시작합니다. (주의: 기존 데이터는 모두 삭제됩니다)
DROP TABLE IF EXISTS transactions CASCADE;
DROP TABLE IF EXISTS todos CASCADE;
DROP TABLE IF EXISTS custom_events CASCADE;
DROP TABLE IF EXISTS study_stamps CASCADE;
DROP TABLE IF EXISTS daily_targets CASCADE;
DROP TABLE IF EXISTS profiles CASCADE;

-- 1. profiles 테이블 생성
CREATE TABLE profiles (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    password TEXT NOT NULL DEFAULT '0000',
    is_password_set BOOLEAN DEFAULT false,
    role TEXT NOT NULL DEFAULT 'child'
);

