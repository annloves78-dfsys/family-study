-- 2. transactions (내역) 테이블 생성
CREATE TABLE transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    date_str TEXT NOT NULL,
    title TEXT NOT NULL,
    amount INTEGER DEFAULT 0,
    coupons INTEGER DEFAULT 0,
    is_deduct BOOLEAN DEFAULT false
);

-- 3. todos (학습 계획표) 테이블 생성
CREATE TABLE todos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    date_str TEXT NOT NULL,
    task_text TEXT NOT NULL,
    is_completed BOOLEAN DEFAULT false
);

-- 4. custom_events (사용자 지정 달력 일정) 테이블 생성
CREATE TABLE custom_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT REFERENCES profiles(id) ON DELETE CASCADE, -- null이면 전체 공통 일정
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    date_str TEXT NOT NULL, -- YYYYMMDD
    event_text TEXT NOT NULL
);

-- =========================================================
-- (선택 사항) Row Level Security (RLS) 정책 임시 해제
-- 본 프로젝트는 가족용이므로 빠른 테스트를 위해 모든 접근을 허용합니다.
-- =========================================================
ALTER TABLE profiles DISABLE ROW LEVEL SECURITY;
ALTER TABLE transactions DISABLE ROW LEVEL SECURITY;
ALTER TABLE todos DISABLE ROW LEVEL SECURITY;
ALTER TABLE custom_events DISABLE ROW LEVEL SECURITY;
